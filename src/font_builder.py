#!/usr/bin/env python3

def print_font(f):
    lines = f.strip("\n").split("\n")

    result = []
    for char in range(13):
        r = n = 0
        for x in range(5):
            for y in range(6):
                if lines[y][x + char * (5+1)] == "#":
                    r |= 1<<n
                n += 1
        result.append(r)
    print("const uint data[] = uint[](" + ",".join([f"{hex(x)}u" for x in result]) + ");")

